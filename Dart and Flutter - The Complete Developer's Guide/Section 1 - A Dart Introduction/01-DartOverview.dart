
//Few Notes on Dart

//Object-oriented language
//  -Most of our time is spent thinking about how to organize code
//   in objects, or 'classes'

//Statically typed
//  -Variables contain data of a single 'type', like an integer or a string

//C-Style Syntax
//  -Syntax of the language is very similar to C, C#, Javascript

//Multiple Runtime Environments
//  -Transpiled to Javascript to run in the browser.
//   Runs in the 'Dart VM' to execute from a command line.
//   Compiled to machine code to run on mobile devices.