 int main() {
    /*While*/
    int i = 0;
    while(i < 3) {
        printf("%i", i);
        i++;
    }
    
    /*For*/
    int i;
    for(i = 0; i < 3; i++)
        printf("%i", i);
    
    /*Do-While*/
    i = 0;
    do {
        printf("I love open source!");
        i++;
    } while(i < 42 );
 }
